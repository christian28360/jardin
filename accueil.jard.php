<h5>Pourquoi ce site ?</h5>

<p>J'ai fait ce site parce que j'en avais assez de manger des produits
	de plus en plus "chimique", de plus en plus fades, sans goût ni
	texture.</p>
<p>J'ai la chance d'avoir un petit bout de jardin près de la maison. Au
	fil des ans je me suis aperçu que je pratiquais la culture non pas
	biologique (celle qu'on trouve sur les marchés), mais naturelle, un peu
	comme elle devait se pratiquer dans le temps où les hommes s'adaptaient
	encore à la nature, et non l'inverse comme de nos jours !</p>
<p>Après quelques recherches sur internet, j'ai monté ces quelques pages
	de conseils pour une nouvelle approche.</p>
<p>Le parcours et long et parfois difficile. Cela demande pas mal
	d'huile de coude et de présence. Mais quel bonheur pour les papilles et
	quels bienfaits pour la santé, tant physique que morale.</p>

